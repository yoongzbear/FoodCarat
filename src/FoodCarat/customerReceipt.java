    private void displayOrderDetails() {
        Order order = new Order(orderType, User.getSessionEmail());
        String[] tokens = order.getOrder(orderID); // This might throw IOException
        //Extract the relevant parameters
        String orderType = tokens[1];
        String orderItems = tokens[2];
        String orderStatus = tokens[3];
        String customerEmail = tokens[4];
        String deliveryFee = tokens[7];
        String paidTotal = tokens[8];
        String orderDate = tokens[9];
        //Get customer point balance and customer contact
        Customer customer = new Customer(User.getSessionEmail());
        int pointBalance = customer.getPoints();
        String creditBalance = "0";
        User user = new User(User.getSessionEmail());
        String[] userInfo = user.performSearch(User.getSessionEmail(), "resources/user.txt");
        String userContact = userInfo[5];
        
        //Display
        sOrderID.setText(String.valueOf(orderID));
        sOrderDate.setText(orderDate);
        sUsername.setText(User.getSessionName());
        sHPNum.setText(userContact);
        sOrderType.setText(String.valueOf(orderType));
        //calculate redeemed points here
        double deliveryFeeDouble = Double.parseDouble(deliveryFee.replace("RM ", ""));
        double paidTotalDouble = Double.parseDouble(paidTotal.replace("RM ", ""));
        double subtotal = populateOrderItemsTable(orderItems);
        double subtotalAfterRedeem = paidTotalDouble - deliveryFeeDouble;
        double redeemedAmount = subtotal - subtotalAfterRedeem;
        double redeemedPoints = redeemedAmount / 0.01;
        //display the values
        sDeliveryFee.setText("RM " + String.format("%.2f", deliveryFeeDouble));
        sPayTotal.setText("RM " + String.format("%.2f", paidTotalDouble));
        sRedeemedPoint.setText(String.format("%.0f", redeemedPoints));
    }
    
    private double populateOrderItemsTable(String orderItemsString) { //to set the order table, vendor name and calculate subtotal
        DefaultTableModel model = (DefaultTableModel) tOrderItem.getModel();
        model.setRowCount(0);  //clear table
        double subtotal = 0.0; //for subtotal calculation

        //retrieve the order item
        orderItemsString = orderItemsString.replace("[", "").replace("]", "");
        String[] items = orderItemsString.split("\\|");

        // Loop through each item and add it to the table
        for (String item : items) {
            // Split each item by the semicolon to get itemID and quantity
            String[] itemDetails = item.split(";");
            if (itemDetails.length == 2) {
                int itemID = Integer.parseInt(itemDetails[0]);
                String quantity = itemDetails[1];
                
                Item item1 = new Item();
                String[] itemData = item1.itemData(itemID);
                String itemName = itemData[1];  
                double itemPrice = Double.parseDouble(itemData[3]);
                double orderItemTotal = Integer.parseInt(quantity) * itemPrice;
                subtotal += orderItemTotal;
                
                String[] vendorInfo = item1.getVendorInfoByItemID(itemID);
                String vendorName = vendorInfo[1];
                sVendorName.setText(String.valueOf(vendorName));

                // Add the item to the table
                model.addRow(new Object[]{itemName, "RM " + itemPrice, quantity, "RM " + orderItemTotal});
            }
        }
        sSubTotal.setText("RM " + String.format("%.2f", subtotal));
        return subtotal;
    }

